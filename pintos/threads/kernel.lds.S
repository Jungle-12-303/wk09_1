/*=======================================================================
 * kernel.lds.S — 커널 링커 스크립트
 *=======================================================================
 *
 * ▶ 역할
 *   링커(ld)가 오브젝트 파일(.o)들을 하나의 커널 바이너리로 합칠 때,
 *   각 섹션(.text, .rodata, .data, .bss)을 메모리의 어디에 배치할지
 *   결정하는 "지도(map)" 역할을 한다.
 *
 * ▶ 핵심 개념: 가상 주소 vs 물리 주소
 *   - 가상 주소(VMA) : 코드가 실행 중일 때 CPU가 보는 주소.
 *     Pintos 커널은 LOADER_KERN_BASE(= 0x8004000000) 에서 시작한다.
 *   - 물리 주소(LMA) : 메모리에 실제로 로드되는 주소.
 *     loader.S가 디스크에서 커널을 읽어 LOADER_PHYS_BASE(= 0x100000,
 *     즉 1MB)에 적재한다.
 *   - start.S에서 페이징을 켜면 VMA ↔ LMA 매핑이 유효해진다.
 *
 * ▶ 메모리 레이아웃 (가상 주소 기준)
 *   ┌─────────────────────────────┐  LOADER_KERN_BASE + LOADER_PHYS_BASE
 *   │  .text   (코드)             │  ← 실행 가능, 읽기 전용
 *   │  .entry  (부트스트랩 진입점) │
 *   ├─────────────────────────────┤
 *   │  .rodata (읽기 전용 데이터)  │  ← 문자열 리터럴, const 변수
 *   ├───────── ALIGN(0x1000) ─────┤  ← 페이지 경계 정렬
 *   │  _end_kernel_text           │  ← 텍스트 영역 끝 마커
 *   ├─────────────────────────────┤
 *   │  .data   (초기화된 데이터)   │  ← 전역/정적 변수 (초깃값 있음)
 *   ├─────────────────────────────┤
 *   │  .bss    (제로-초기화 데이터)│  ← 전역/정적 변수 (초깃값 0)
 *   ├─────────────────────────────┤
 *   │  _end                       │  ← 커널 이미지 끝
 *   └─────────────────────────────┘
 *
 * ▶ Project 1 관련
 *   직접 수정할 일은 없지만, palloc.c가 _end 심볼을 사용하여
 *   커널 이미지 뒤의 빈 메모리를 페이지 풀(pool)로 초기화한다.
 *   따라서 이 스크립트가 어떻게 _end를 정의하는지 이해하면
 *   메모리 관리 전체 그림이 보인다.
 *=======================================================================*/

#include "threads/loader.h"

/*-- 출력 바이너리 형식: 64비트 ELF (x86-64) --*/
OUTPUT_FORMAT("elf64-x86-64")
OUTPUT_ARCH(i386:x86-64)

/*-- 커널 진입점: start.S의 _start 심볼 --*/
ENTRY(_start)

SECTIONS
{
  /*--------------------------------------------------------------------
   * 가상 주소 시작점 설정
   *   LOADER_KERN_BASE = 0x8004000000  (커널 가상 베이스)
   *   LOADER_PHYS_BASE = 0x100000      (1MB, 물리 로드 주소)
   *   즉, 커널 코드의 가상 주소는 0x8004100000 부터 시작한다.
   *--------------------------------------------------------------------*/
	. = LOADER_KERN_BASE + LOADER_PHYS_BASE;

	/*-- start 심볼을 현재 위치(.) 로 제공 --*/
	PROVIDE(start = .);

  /*--------------------------------------------------------------------
   * .text 섹션 (코드)
   *   AT(LOADER_PHYS_BASE) → 물리 주소는 1MB부터 시작
   *   .entry  : start.S의 bootstrap 코드 (가장 먼저 배치)
   *   .text   : 나머지 모든 C/어셈블리 코드
   *   = 0x90  : 빈 공간을 NOP(0x90) 으로 채운다 (디버깅 안전장치)
   *--------------------------------------------------------------------*/
	.text : AT(LOADER_PHYS_BASE) {
		*(.entry)
		*(.text .text.* .stub .gnu.linkonce.t.*)
	} = 0x90

  /*--------------------------------------------------------------------
   * .rodata 섹션 (읽기 전용 데이터)
   *   const char* 문자열, switch-case 점프 테이블 등이 여기에 배치된다.
   *--------------------------------------------------------------------*/
	.rodata         : { *(.rodata .rodata.* .gnu.linkonce.r.*) }

  /*--------------------------------------------------------------------
   * 페이지 경계 정렬 (4KB = 0x1000)
   *   텍스트+rodata 영역과 데이터 영역 사이를 페이지 경계로 나눈다.
   *   이렇게 하면 나중에 텍스트 영역을 읽기 전용으로, 데이터 영역을
   *   읽기-쓰기로 페이지 테이블에서 따로 보호할 수 있다.
   *   _end_kernel_text 심볼로 텍스트 영역 끝 주소를 기록한다.
   *--------------------------------------------------------------------*/
	. = ALIGN(0x1000);
	PROVIDE(_end_kernel_text = .);

  /*--------------------------------------------------------------------
   * .data 섹션 (초기화된 데이터)
   *   예: int ready = 1;  ← 초깃값이 있는 전역 변수
   *--------------------------------------------------------------------*/
  .data : { *(.data) *(.data.*)}

  /*--------------------------------------------------------------------
   * .bss 섹션 (제로-초기화 데이터)
   *   예: int count;  ← 초깃값 없는 전역 변수 (0으로 초기화)
   *   _start_bss ~ _end_bss 범위를 init.c의 bss_init()이 memset(0) 한다.
   *--------------------------------------------------------------------*/
  PROVIDE(_start_bss = .);
  .bss : { *(.bss) }
  PROVIDE(_end_bss = .);

  /*--------------------------------------------------------------------
   * _end: 커널 이미지의 끝 주소
   *   palloc_init()이 이 주소 이후의 메모리를 페이지 풀로 사용한다.
   *   즉, 커널 코드+데이터가 차지하는 공간이 여기서 끝난다.
   *--------------------------------------------------------------------*/
  PROVIDE(_end = .);

  /*--------------------------------------------------------------------
   * /DISCARD/ — 불필요한 섹션 제거
   *   .eh_frame       : C++ 예외 처리용 (Pintos는 C만 사용)
   *   .note.GNU-stack : 스택 실행 권한 관련 메타데이터
   *   .stab           : 레거시 디버그 정보
   *--------------------------------------------------------------------*/
	/DISCARD/ : {
		*(.eh_frame .note.GNU-stack .stab)
	}
}
